    protected void datatable(){
        Object[] Baris = {
            "NIP Wali Kelas",
            "Kode Kelas",
            "Absen",
            "Tidak Absen",
            "Izin/Sakit",
            "Total Siswa"
        };
        
        tabmode = new DefaultTableModel(null, Baris);
        tableAbsen.setModel(tabmode);

        String sql = "SELECT * FROM absen";
        
        try{
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                String a = hasil.getString("wali_kelas_nip");
                String b = hasil.getString("kd_kelas");
                String c = hasil.getString("absen");
                String d = hasil.getString("tidak_absen");
                String e = hasil.getString("izin_sakit");
                String f = hasil.getString("total_siswa");
                
                String[] data={a, b, c, d, e, f};
                tabmode.addRow(data);
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,e);
        }
    }
    protected void getRelasi(){
     
        String sql = "SELECT DISTINCT guru.nip FROM guru left join absen On guru.nip = absen.wali_kelas_nip;";
        
        try{
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                String a = hasil.getString("guru.nip");
                
                String[] data={a};
                CbRelasiNip.addItem(a);
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,e);
        }
    }
    
