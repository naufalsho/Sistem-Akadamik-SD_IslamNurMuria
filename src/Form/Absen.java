    private void bSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSimpanActionPerformed
        // TODO add your handling code here:
         String sql = "INSERT INTO absen VALUES (?,?,?,?,?,?)";
        try{
            PreparedStatement stat = conn.prepareStatement(sql);
            
            stat.setString(1, CbRelasiNip.getSelectedItem().toString());
            stat.setString(2, tKdKelas.getText());
            stat.setString(3, tAbsen.getText());
            stat.setString(4, tTdkAbsen.getText());
            stat.setString(5, tIzinSakit.getText());
            stat.setString(6, tTotalSiswa.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "DATA Berhasil Disimpan");
            kosong();
            tKdKelas.requestFocus();
            datatable();
        }catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan "+e);
        }
    }//GEN-LAST:event_bSimpanActionPerformed

